package com.syntax.class07;

public class IncrementAndDecrement {

	public static void main(String[] args) {
		int num=90;
		num=num+1;
		num+=1;
		System.out.println(num);
		

		num++;// adding 1
		
		System.out.println(num);
		
		int num1=100;
		
		num1-=1;
		num1--;// minus 1b(substract 1)
		System.out.println(num1);

		// 100--;  doesnt work error
		// because increment & decrement operators can be use ONLY with variables;
		
		
		
		System.out.println("----------------------------------------------------------------------");
		
		//insted of writing so many timnes one line 
		System.out.println("Hello");
		System.out.println("Hello");
		System.out.println("Hello");
		System.out.println("Hello");
		
	}

}
