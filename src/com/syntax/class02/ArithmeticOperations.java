		double num4=2.99;
		
		double multi=num3*num4;
		System.out.println(multi);
		
		double div=num3/num4;
		System.out.println("The result of division of 2 double values = " +div);
		
		double num=54.54;
		double num5=68.68;
		
		double line2=num*num5;
		System.out.println(line2);
